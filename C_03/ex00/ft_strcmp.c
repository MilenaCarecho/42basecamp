int	ft_strcmp(char *s1, char *s2)
{
	int	a;
	int	b;

	a = 0;
	b = 0;
	while (s1[a] == s2[a] && s1 != '\0')
	{
		a++;
	}
	b = (unsigned char)s1[a] - (unsigned char)s2[a];
	return (b);
}
